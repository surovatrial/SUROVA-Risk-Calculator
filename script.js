function calculateRisk() { 
    // Verificar que todos los campos estÃ©n completos
    const fields = [
        "age", "ecog", "stageIV", "treatment", "ascites", "parp", "residual", "complications", "complicationsGreaterGrade3"
    ];

    let allFieldsFilled = true;
    fields.forEach(field => {
        const value = document.getElementById(field).value;
        if (!value) {
            allFieldsFilled = false;
        }
    });

    if (!allFieldsFilled) {
        alert('Please complete all fields before calculating the risk.');
        return;
    }

    // Coefficients for Logistic Regression
    const logisticCoefficients = {
        intercept: -1.162,
        age: 0.409,
        ecog: 0.193,
        stageIV: 0.192,
        treatment: 0.344,
        ascites: 0.259,
        parp: -0.396,
        residual: 0.458,
        complications: 0.427,
        complicationsGreaterGrade3: 0.107
    };

    // Coefficients for Cox Regression
    const coxCoefficients = {
        age: 0.298,
        ecog: 0.220,
        stageIV: 0.124,
        treatment: 0.254,
        ascites: 0.268,
        parp: -0.699,
        residual: 0.524,
        complications: 0.284,
        complicationsGreaterGrade3: 0.252
    };
    
    // Get values from inputs
    const age = parseFloat(document.getElementById("age").value) || 0;
    const ecog = parseFloat(document.getElementById("ecog").value) || 0;
    const stageIV = parseFloat(document.getElementById("stageIV").value) || 0;
    const treatment = parseFloat(document.getElementById("treatment").value) || 0;
    const ascites = parseFloat(document.getElementById("ascites").value) || 0;
    const parp = parseFloat(document.getElementById("parp").value) || 0;
    const residual = parseFloat(document.getElementById("residual").value) || 0;
    const complications = parseFloat(document.getElementById("complications").value) || 0;
    const complicationsGreaterGrade3 = parseFloat(document.getElementById("complicationsGreaterGrade3").value) || 0;
    
    // Logistic regression formula for risk probability
    const logit = logisticCoefficients.intercept +
        logisticCoefficients.age * age +
        logisticCoefficients.ecog * ecog +
        logisticCoefficients.stageIV * stageIV +
        logisticCoefficients.treatment * treatment +
        logisticCoefficients.ascites * ascites +
        logisticCoefficients.parp * parp +
        logisticCoefficients.residual * residual +
        logisticCoefficients.complications * complications +
        logisticCoefficients.complicationsGreaterGrade3 * complicationsGreaterGrade3;
    
    const probability = 1 / (1 + Math.exp(-logit));
    document.getElementById("result").textContent = (probability * 100).toFixed(2) + "%";

    // Categorize risk
    let category;
    if (probability < 0.33) category = "Low";
    else if (probability < 0.5) category = "Moderate";
    else if (probability < 1) category = "High";
    else category = "Very High";

    document.getElementById("category").textContent = category;

    // Cox regression formula for relative risk
    const logRisk = 
        coxCoefficients.age * age +
        coxCoefficients.ecog * ecog +
        coxCoefficients.stageIV * stageIV +
        coxCoefficients.treatment * treatment +
        coxCoefficients.ascites * ascites +
        coxCoefficients.parp * parp +
        coxCoefficients.residual * residual +
        coxCoefficients.complications * complications +
        coxCoefficients.complicationsGreaterGrade3 * complicationsGreaterGrade3;

    const riskRelative = Math.exp(logRisk);

    // Calculate median survival
    const medianSurvival = calculateMedianSurvival(riskRelative);
    document.getElementById("median-survival").textContent = `${medianSurvival} months`;
}

